#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*temp;
	int		i;

	i = 0;
	while (str[i] != '\0')
		i++;
	temp = (char *)malloc(sizeof(char) * (i + 1));
	if (!temp)
		return (NULL);
	i = 0;
	while (str[i])
	{
		temp[i] = str[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
